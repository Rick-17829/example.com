<h2>Santuario  Virgen de La Piedra</h2>
<h2>Un Lugar de Fe y Naturaleza en Dipilto, Nueva Segovia</h2>
<p>Un Destino de Devoción y Belleza Natural
Ubicado en el pintoresco municipio de Dipilto, en Nueva Segovia, el Santuario Nacional Virgen de La Piedra es un lugar sagrado que atrae a peregrinos y visitantes en busca de paz, oración y contacto con la naturaleza. Este santuario, lleno de historia y espiritualidad, ofrece una experiencia única que combina la devoción religiosa con la belleza natural.
</p>

<h3>Atractivos del Santuario</h3>
<h4>
  El Santuario Virgen de La Piedra cuenta con varios puntos de interés que enriquecen la visita:
</h4>

<ol>Esculturas Religiosas: Observa y contempla diversas esculturas que adornan el santuario, reflejando la profunda religiosidad del lugar</ol>
<ol>El Río y el Pozo de Agua Bendita: Disfruta de la tranquilidad del río y visita el pozo que contiene agua bendita, un lugar de oración y recogimiento</ol>
<ol>Tienda de Artículos Religiosos: Compra recuerdos y artículos religiosos en la tienda de piedra del santuario</ol>
<h3>La Imagen de la Virgen de Guadalupe</h3>
<p>
  La imagen de la Virgen de Guadalupe fue empotrada en la roca en 1947 por el sacerdote monseñor Nicolás Antonio Madrigal, quien está en proceso de canonización. Durante un tiempo en que el municipio estaba siendo azotado por una enfermedad similar a la lepra, la colocación de la imagen y las oraciones fervientes de los habitantes llevaron a recibir numerosos favores y curaciones, convirtiendo el lugar en un centro de peregrinación y fe.
</p>
